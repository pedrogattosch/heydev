// Operadores de comaparação
/*
Igual (==) Retorna true caso os operando sejam iguais.
Não igual (!=) Retorna true caso os operando não sejam iguais.
Estritamente igual (===) Retorna true caso os operando sejam iguais e do mesmo tipo.
Estritamente não igual (!==) Retorna true caso os operando não sejam iguais e do mesmo tipo.
Maior que (>) Retorna true caso o operando da esquerda seja maior que o da direita.
Maior que ou igual (>=) Retorna true caso o operando da esquerda seja maior ou igual o da direita.
Menor que (>) Retorna true caso o operando da esquerda seja menor que o da direita.
Menor que ou igual (>=) Retorna true caso o operando da esquerda seja menor ou igual o da direita.
*/